import UIKit

class DetailsTableViewController: UITableViewController {

    var pokemon:Pokemon!
    var pokemonIndex:Int!
    let repository:PokemonRepository = PokemonRepository()
    var delegate:ListTableViewController!
    
    @IBOutlet weak var imageFrontDefault: UIImageView!
    
    override func viewDidLoad() {
        super.viewDidLoad()
        if (pokemon != nil) {
            self.title = self.pokemon.name
            repository.get(pokemon.url) {
                pokemon in
                self.pokemon = pokemon
                self.refresh()
            }
        }
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }
    
    override func viewWillDisappear(animated: Bool) {
        super.viewWillDisappear(animated)
        self.delegate.updatePokemonFromDetails(self.pokemon, index: self.pokemonIndex)
    }
    
    func refresh() {
        if pokemon.image != nil {
            self.imageFrontDefault.image = pokemon.image
        } else {
            ImageHelper.loadImageFromUrl(self.pokemon.sprites.frontDefault, view: self.imageFrontDefault) {
                image in
                self.pokemon.image = image
            }
        }
    }
    

